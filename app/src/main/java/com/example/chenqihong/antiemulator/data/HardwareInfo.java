public class HardwareInfo {
    private String board;
    private String bootLoader;
    private String brand;
    private String device;
    private String model;
    private String product;
    private String hardware;
    private String roSerialNo;
    private String roHardware;

    public String getBoard() {
        return board;
    }

    public void setBoard(String board) {
        this.board = board;
    }

    public String getBootLoader() {
        return bootLoader;
    }

    public void setBootLoader(String bootLoader) {
        this.bootLoader = bootLoader;
    }

    public String getBrand() {
        return brand;
    }

    public void setBrand(String brand) {
        this.brand = brand;
    }

    public String getDevice() {
        return device;
    }

    public void setDevice(String device) {
        this.device = device;
    }

    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }

    public String getProduct() {
        return product;
    }

    public void setProduct(String product) {
        this.product = product;
    }

    public String getHardware() {
        return hardware;
    }

    public void setHardware(String hardware) {
        this.hardware = hardware;
    }

    public String getRoSerialNo() {
        return roSerialNo;
    }

    public void setRoSerialNo(String roSerialNo) {
        this.roSerialNo = roSerialNo;
    }

    public String getRoHardware() {
        return roHardware;
    }

    public void setRoHardware(String roHardware) {
        this.roHardware = roHardware;
    }
}
